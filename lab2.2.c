#include <stdio.h>

int main() {
    // Оголошення та ініціалізація змінних
    int a = 15, b = -2, c = 3;
    float y;

    // Обчислення значення y за заданою формулою
    y = (3*a - 4*b + 5*c) / (a*b + 2*b*c + 3*a*c) - a*b - c/4.0;

    // Виведення значення y на консоль з точністю до двох знаків після коми
    printf("Значення y є %.2f\n", y);

    // Повернення значення 0 для позначення успішного виконання програми
    return 0;
}